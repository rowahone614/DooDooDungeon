using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace DooDooDungeon
{
    public class Grate
    {
        //Integer declaration for the grate object
        public int x, y, size;

        public Grate(int _grateX, int _grateY, int _grateSize)
        {
            //Attribute assignment
            x = _grateX;
            y = _grateY;
            size = _grateSize;
        }
    }
}
